package com.jdyxtech.jindouyunxing.javabean;

import java.util.List;

public class MyIntegrals {
	private int status;
	private float sum;
	private String desc;
	private List<Integral> lists;
	
	public int getStatus() {
		return status;
	}
	public void setStatus(int status) {
		this.status = status;
	}
	public float getSum() {
		return sum;
	}
	public void setSum(float sum) {
		this.sum = sum;
	}
	public String getDesc() {
		return desc;
	}
	public void setDesc(String desc) {
		this.desc = desc;
	}
	public List<Integral> getLists() {
		return lists;
	}
	public void setLists(List<Integral> lists) {
		this.lists = lists;
	}

}
